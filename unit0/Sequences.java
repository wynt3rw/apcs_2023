public class Sequences{
    public static void main(String[] args){
        int n = 5;
        System.out.println(n);
        
        n = 23;
        System.out.println(n);
        
        float x = 1.23f;
        System.out.println(x);
        
        double y = 2.34;
        System.out.println(y);
        
        boolean happy = true;
        System.out.println(happy);
        
        String name = "Wynter W.";
        System.out.println(name);
        
        System.out.println();
    }
}